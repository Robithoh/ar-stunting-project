
public class DataIbuMenyusui
{
    public string nama;
    public string umur;
    public string tAsi;
    public int beratBadan;
    public string hasilRekomendasi;

    public DataIbuMenyusui(string _nama, string _umur, string _tAsi, int _beratBadan, string _hasilRekomendasi)
    {
        this.nama = _nama;
        this.umur = _umur;
        this.tAsi = _tAsi;
        this.beratBadan = _beratBadan;
        this.hasilRekomendasi = _hasilRekomendasi;
    }
}