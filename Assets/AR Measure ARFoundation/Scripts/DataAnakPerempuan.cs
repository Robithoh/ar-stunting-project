public class DataAnakPerempuan
{
    public int umur;
    public int tinggiBadan;
    public string statGizi;
    public string hasilRekomendasi;

    public DataAnakPerempuan(int _umur, int _tinggiBadan, string _statGizi, string _hasilRekomendasi)
    {
        this.umur = _umur;
        this.tinggiBadan = _tinggiBadan;
        this.statGizi = _statGizi;
        this.hasilRekomendasi = _hasilRekomendasi;
    }
}